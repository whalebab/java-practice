package java_basic.class1;

// 클래스에 정의한 변수들을 멤버변수, 또는 필드
// 멤버 변수(Member Variable): 이 변수들은 특정 클래스에 소속된 멤버이기 때문에 이렇게 부른다
// 필드(Field): 데이터 항목을 가르키는 정통적인 용어
// 멤버 변수 == 필드 (클래스에 소속된 변수를 뜻한다.)

// 설계도(클래스)를 사용해서 실제 메모리에 만들어진 실체를 객체 또는 인스턴스라 한다.
// 클래스를 통해서 사용자가 원하는 종류의 데이터 타입을 마음껏 정의할 수 있다.
public class Student {
    String name;
    int age;
    int grade;
}
